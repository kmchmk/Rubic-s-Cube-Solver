    void ChangeColour(JButton button) {
        Color nextColour;
        Color colour = button.getBackground();
        if (colour == Color.white) {
            nextColour = Color.red;
        } else if (colour == Color.red) {
            nextColour = Color.yellow;
        } else if (colour == Color.yellow) {
            nextColour = Color.orange;
        } else if (colour == Color.orange) {
            nextColour = Color.blue;
        } else if (colour == Color.blue) {
            nextColour = Color.green;
        } else if (colour == Color.green) {
            nextColour = Color.white;
        } else {
            nextColour = Color.white;
        }
        button.setBackground(nextColour);
    }

    void getColours() {
        Face selectedFace;
        if (jRadioButton1.isSelected()) {
            selectedFace = cube.Front;
        } else if (jRadioButton2.isSelected()) {
            selectedFace = cube.Back;
        } else if (jRadioButton3.isSelected()) {
            selectedFace = cube.Up;
        } else if (jRadioButton4.isSelected()) {
            selectedFace = cube.Down;
        } else if (jRadioButton5.isSelected()) {
            selectedFace = cube.Right;
        } else if (jRadioButton6.isSelected()) {
            selectedFace = cube.Left;
        } else {
            jLabel1.setText("Select a Face");
            selectedFace = cube.Front;//nothing else to do
        }

        //change the colours in the algorithm
        selectedFace.LeftUp.Colour = jButton1.getBackground();
        selectedFace.MiddleUp.Colour = jButton2.getBackground();
        selectedFace.RightUp.Colour = jButton3.getBackground();
        selectedFace.LeftMiddle.Colour = jButton4.getBackground();
        selectedFace.MiddleMiddle.Colour = jButton5.getBackground();
        selectedFace.RightMiddle.Colour = jButton6.getBackground();
        selectedFace.LeftDown.Colour = jButton7.getBackground();
        selectedFace.MiddleDown.Colour = jButton8.getBackground();
        selectedFace.RightDown.Colour = jButton9.getBackground();
    }

    void AddColoursToInterface() {
        Face selectedSide;
        if (jRadioButton1.isSelected()) {
            selectedSide = cube.Front;
        } else if (jRadioButton2.isSelected()) {
            selectedSide = cube.Back;
        } else if (jRadioButton3.isSelected()) {
            selectedSide = cube.Up;
        } else if (jRadioButton4.isSelected()) {
            selectedSide = cube.Down;
        } else if (jRadioButton5.isSelected()) {
            selectedSide = cube.Right;
        } else if (jRadioButton6.isSelected()) {
            selectedSide = cube.Left;
        } else {
            selectedSide = cube.Front;
        }

        jButton1.setBackground(selectedSide.LeftUp.Colour);
        jButton2.setBackground(selectedSide.MiddleUp.Colour);
        jButton3.setBackground(selectedSide.RightUp.Colour);
        jButton4.setBackground(selectedSide.LeftMiddle.Colour);
        jButton5.setBackground(selectedSide.MiddleMiddle.Colour);
        jButton6.setBackground(selectedSide.RightMiddle.Colour);
        jButton7.setBackground(selectedSide.LeftDown.Colour);
        jButton8.setBackground(selectedSide.MiddleDown.Colour);
        jButton9.setBackground(selectedSide.RightDown.Colour);
    }

